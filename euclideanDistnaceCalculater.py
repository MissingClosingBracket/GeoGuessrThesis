from math import sqrt
from math import pow
from helperFunctions import getDistance

#eucl
def calc(p1,p2):
    x1=p1[0]
    y1=p1[1]
    x2=p2[0]
    y2=p2[1]

    dist = sqrt(
        pow(x1-x2,2) +
        pow(y1-y2,2)
    )

    return dist

